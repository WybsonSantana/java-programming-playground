public class ArithmeticOperators {
    public static void main(String[] args) {
        int myAge = 120;
        int yourAge = 130;

        // Addition
        int totalAge = myAge + yourAge + 100;
        System.out.println("Sum of ages: " + totalAge);

        // Subtraction
        int ageDiference = yourAge - myAge;
        System.out.println("Age difference: " + ageDiference);

        // Multiplication
        int twiceAge = yourAge * 2;
        System.out.println("Twice age: " + twiceAge);

        // Division
        int halfAge = yourAge / 2;
        System.out.println("Half age: " + halfAge);
    }
}
